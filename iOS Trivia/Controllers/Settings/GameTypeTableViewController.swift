        return 1
    }

    override func tableView(_ tableView: UITableView, numberOfRowsInSection section: Int) -> Int {
        // #warning Incomplete implementation, return the number of rows
        return gameTypeData.dataArray.count
    }

    override func tableView(_ tableView: UITableView, cellForRowAt indexPath: IndexPath) -> UITableViewCell {
        let cell = tableView.dequeueReusableCell(withIdentifier: "cell", for: indexPath)
        cell.textLabel?.text = gameTypeData.dataArray[indexPath.row]["name"]
        cell.textLabel?.textColor = Color.darkOrange
        if gameTypeData.dataArray[indexPath.row]["name"] == gameTypeData.selectedName {
            cell.accessoryType = .checkmark
            cell.tintColor = Color.darkOrange
        } else {
            cell.accessoryType = .none
        }
        return cell
    }
    
    override func tableView(_ tableView: UITableView, didSelectRowAt indexPath: IndexPath) {
        tableView.deselectRow(at: indexPath, animated: true)
        let dict = gameTypeData.dataArray[indexPath.row]
        delegate?.didSelecte(name: dict["name"] ?? "Any", value: dict["value"] ?? "any")
        navigationController?.popViewController(animated: true)
    }

