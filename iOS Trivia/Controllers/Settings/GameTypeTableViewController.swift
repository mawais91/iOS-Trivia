import UIKit

protocol GameTypeDelegate: class {
    func didSelecte(name: String, value: String)
}

class GameTypeTableViewController: UITableViewController {
    
    var gameTypeData: GameTypeData!
    weak var delegate: GameTypeDelegate?
    
    override func viewDidLoad() {
        super.viewDidLoad()
        title = gameTypeData.title
        tableView.register(UITableViewCell.self, forCellReuseIdentifier: "cell")
    }

    // MARK: - Table view data source

    override func numberOfSections(in tableView: UITableView) -> Int {
